namespace LazyLogNet;

/// <summary>
/// 日志级别枚举
/// </summary>
public enum LazyLogLevel
{
    /// <summary>
    /// 调试级别日志
    /// </summary>
    Debug = 0,

    /// <summary>
    /// 信息级别日志
    /// </summary>
    Info = 1,

    /// <summary>
    /// 警告级别日志
    /// </summary>
    Warn = 2,

    /// <summary>
    /// 错误级别日志
    /// </summary>
    Error = 3,

    /// <summary>
    /// 致命错误级别日志
    /// </summary>
    Fatal = 4
}